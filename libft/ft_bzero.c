#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*sr;

	i = 0;
	sr = (char *)s;
	while (i < n)
	{
		sr[i] = '\0';
		i++;
	}
	return (*s);
}
